import { Position } from "@/types/position";

function shuffleArray<T>(array: T[]): T[] {
  const shuffled = [...array]; // Crear una copia para no mutar el original
  for (let i = shuffled.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [shuffled[i], shuffled[j]] = [shuffled[j], shuffled[i]]; // Intercambiar elementos
  }
  return shuffled;
}

function generateRandomStats() {
  const availableSlots = Math.floor(Math.random() * 5) + 2; // 2-6
  const acceptedApplicants = Math.floor(Math.random() * availableSlots); // 0 to availableSlots-1
  const currentApplicants = Math.floor(Math.random() * 16) + 5; // 5-20
  return { availableSlots, acceptedApplicants, currentApplicants };
}

function addStatsToPosition(position: any): Position {
  const stats = generateRandomStats();
  if (position.status === "closed") {
    // Para posiciones cerradas, aceptados = cupos (todos los cupos están llenos)
    return { 
      ...position, 
      ...stats,
      acceptedApplicants: stats.availableSlots 
    };
  }
  return { ...position, ...stats };
}

